/*
================
GL_MipMap

Operates in place, quartering the size of the texture
================
*/
void GL_MipMap (byte *in, int width, int height)
{
	int			out_width, out_height;
	int			x, y;
	byte*		src[2];
	byte*		dst;

	out_width  = width  >> 1;
	out_height = height >> 1;

	dst = in;
	for (y = 0; y < out_height; y++)
	{
		src[0] = in + ( (y * width ) << 3);
		src[1] = src[0] + (width << 2);
		for (x = 0; x < out_width; x++, dst += 4, src[0] += 8, src[1] += 8)
		{
			dst[0] = (src[0][0] + src[0][4] + src[1][0] + src[1][4])>>2;
			dst[1] = (src[0][1] + src[0][5] + src[1][1] + src[1][5])>>2;
			dst[2] = (src[0][2] + src[0][6] + src[1][2] + src[1][6])>>2;
			dst[3] = (src[0][3] + src[0][7] + src[1][3] + src[1][7])>>2;
		}
	}
}


/*
===============
GL_Upload32
===============
*/
void GL_Upload32 (unsigned *data, int width, int height, qboolean mipmap, qboolean alpha)
{
	int			samples;

	if (width  > gl_max_size.value ||
		height > gl_max_size.value)
		Sys_Error ("GL_LoadTexture: too big");


	samples = alpha ? gl_alpha_format : gl_solid_format;

	texels += width * height;

	if (!mipmap)
	{
		glTexImage2D (GL_TEXTURE_2D, 0, samples, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);
		goto done;
	}

	glTexImage2D (GL_TEXTURE_2D, 0, samples, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);
	if (mipmap)
	{
		int		miplevel;

		miplevel = 0;
		while (width > 1 || height > 1)
		{
			GL_MipMap ((byte *)data, width, height);
			width  >>= 1;
			height >>= 1;
			if (width  < 1)
				width  = 1;
			if (height < 1)
				height = 1;
			miplevel++;
			glTexImage2D (GL_TEXTURE_2D, miplevel, samples, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);
		}
	}
done: ;

	if (mipmap)
	{
		glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, gl_filter_min);
		glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, gl_filter_max);
	}
	else
	{
		glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, gl_filter_max);
		glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, gl_filter_max);
	}
}

/*
===============
GL_Upload8
===============
*/
void GL_Upload8 (byte *data, int width, int height,  qboolean mipmap, qboolean alpha)
{
	const byte			c_first_fullbrigh = 256 - 32;
	static	unsigned	trans[1024*1024];		// FIXME, temporary
	int			i, s;
	qboolean	noalpha;

	s = width*height;
	if (s > sizeof(trans))
		Sys_Error ("GL_Upload8: temporary buffer too small\n");

	if (alpha)
	{
		for (i=0 ; i<s ; i++)
			trans[i] = d_8to24table[data[i]];
	}
	else
	{
		// Mark bright colors as zero alpha in textures for shader.
		if (s&3)
			for (i=0 ; i<s ; i++)
			{
				trans[i] = d_8to24table[data[i]];
				if( data[i] >= c_first_fullbrigh ) trans[i] &= 0x00FFFFFF;
			}
		else
			for (i=0 ; i<s ; i+=4)
			{
				trans[i  ] = d_8to24table[data[i  ]];
				if( data[i  ] >= c_first_fullbrigh ) trans[i  ] &= 0x00FFFFFF;

				trans[i+1] = d_8to24table[data[i+1]];
				if( data[i+1] >= c_first_fullbrigh ) trans[i+1] &= 0x00FFFFFF;

				trans[i+2] = d_8to24table[data[i+2]];
				if( data[i+2] >= c_first_fullbrigh ) trans[i+2] &= 0x00FFFFFF;

				trans[i+3] = d_8to24table[data[i+3]];
				if( data[i+3] >= c_first_fullbrigh ) trans[i+3] &= 0x00FFFFFF;
			}
	}

	GL_Upload32 (trans, width, height, mipmap, alpha);
}

/*
================
GL_LoadTexture
================
*/
int GL_LoadTexture (char *identifier, int width, int height, byte *data, qboolean mipmap, qboolean alpha)
{
	qboolean	noalpha;
	int			i, p, s;
	gltexture_t	*glt;

	// see if the texture is allready present
	if (identifier[0])
	{
		for (i=0, glt=gltextures ; i<numgltextures ; i++, glt++)
		{
			if (!strcmp (identifier, glt->identifier))
			{
				if (width != glt->width || height != glt->height)
					Sys_Error ("GL_LoadTexture: cache mismatch");
				return gltextures[i].texnum;
			}
		}
	}
	else {
		glt = &gltextures[numgltextures];
		numgltextures++;
	}

	strcpy (glt->identifier, identifier);
	glGenTextures( 1, &glt->texnum );
	glt->width = width;
	glt->height = height;
	glt->mipmap = mipmap;

	GL_Bind( glt->texnum );

	GL_Upload8 (data, width, height, mipmap, alpha);

	return glt->texnum;
}

/*
================
GL_LoadPicTexture
================
*/
int GL_LoadPicTexture (qpic_t *pic)
{
	return GL_LoadTexture ("", pic->width, pic->height, pic->data, false, true);
}

