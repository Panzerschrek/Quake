static GLenum oldtarget = GL_TEXTURE0;

void GL_SelectTexture (GLenum target) 
{
	if (target == oldtarget) 
		return;

	glActiveTexture( target );

	cnttextures[oldtarget-GL_TEXTURE0] = currenttexture;
	currenttexture = cnttextures[target-GL_TEXTURE0];
	oldtarget = target;
}
