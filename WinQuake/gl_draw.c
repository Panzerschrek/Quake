/*
================
Draw_String
================
*/
void Draw_String (int x, int y, char *str)
{
	while (*str)
	{
		Draw_Character (x, y, *str);
		str++;
		x += 8;
	}
}

/*
================
Draw_StringScaled
================
*/
void Draw_StringScaled (int x, int y, int scale, char *str)
{
	if (scale == 1)
	{
		Draw_String	(x, y, str);
		return;
	}

	while (*str)
	{
		Draw_CharacterScaled (x, y, scale, *str);
		str++;
		x += 8 * scale;
	}
}

